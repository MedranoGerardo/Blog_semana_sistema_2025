                <h3>Actividades del Día</h3>
                
                <div class="activity">
                    <h4>Taller 1: Toma de decisiones estratégicas basadas en datos</h4>
                    <p><strong>Ponente:</strong> Msc. Enrique Cortez</p>
                    <p><strong>Hora:</strong> 8:00 AM - 9:40 AM</p>
                    <p><strong>Lugar:</strong> Virtual</p>
                    <p>
                        Se habló sobre los bloques académicos y la alta demanda de la carrera de Ingeniería en Sistemas, destacando su 
                        relevancia en el mercado laboral actual. Durante la actividad se realizó un ejercicio práctico en Excel con una base
                        de 10,000 datos, aplicando filtros de información para identificar las ciudades y departamentos de las sucursales, 
                        además de analizar los ID de vendedores y obtener un recuento por sucursal. También se elaboró un análisis comparativo 
                        para determinar qué sucursal registraba mayores ventas. Finalmente, se mencionó Claude IA como una de las herramientas 
                        más utilizadas por los programadores, y se recomendaron plataformas educativas como Khan Academy y Midudev para el 
                        aprendizaje continuo.
                    </p>
                </div>
                
                <div class="activity">
                    <h4>Taller 2: " Developers 2.0: Entrenando la IA que Programa Contigo"</h4>
                    <p><strong>Ponente:</strong> Ing. Henry Orellana</p>
                    <p><strong>Hora:</strong> 10:00 AM - 12:00 PM</p>
                    <p><strong>Lugar:</strong> Virtual</p>
                    <p>
                        Fue un taller enfocado en la integración de la inteligencia artificial en el desarrollo de software moderno. Se abordó 
                        cómo las nuevas herramientas de IA están cambiando la forma en que los programadores escriben código, optimizan procesos y
                        resuelven problemas. El enfoque principal fue comprender cómo entrenar y aprovechar modelos de IA para que trabajen como 
                        asistentes de programación, potenciando la productividad y la creatividad de los desarrolladores.
                    </p>
                </div>
                
                <div class="activity">
                    <h4>Taller 2.2: "Construyendo un CRUD en minutos con Laravel y Filament"</h4>
                    <p><strong>Ponente:</strong> Ing. Gabriel Castilllo</p>
                    <p><strong>Hora:</strong> 10:00 AM - 12:00 PM</p>
                    <p><strong>Lugar:</strong> Virtual</p>
                    <p>
                        Este taller se centró en el desarrollo y práctica de un CRUD (Crear, Leer, Actualizar y Eliminar) utilizando el framework 
                        Laravel. Durante la sesión se explicó la estructura básica del framework, se realizaron ejercicios prácticos para la gestión 
                        de datos y se destacó la importancia de una buena documentación en los proyectos para mantener un código claro, ordenado y 
                        fácil de mantener. Además, se exploró el uso de Filament, una herramienta moderna para paneles administrativos en Laravel, y 
                        se revisó su documentación oficial para comprender su configuración y funcionalidades principales.
                    </p>
                </div>

                <div class="activity">
                    <h4>Taller 3: "Técnicas de Programación"</h4>
                    <p><strong>Ponente:</strong> Dr. Oscar López</p>
                    <p><strong>Hora:</strong> 1:00 PM - 2:40 PM</p>
                    <p><strong>Lugar:</strong> Virtual</p>
                    <p>
                        Proximamente más información. El ponente no llego a tiempo.
