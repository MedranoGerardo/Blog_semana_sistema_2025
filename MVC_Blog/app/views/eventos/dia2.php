                <div class="activity">
                    <h4>Taller 4: "IA Multimodal: Creando el futuro visual e inteligencia con n8n De texto a imagen que conectan y transforman"</h4>
                    <p><strong>Ponente:</strong> Msc. Jose Salmerón</p>
                    <p><strong>Hora:</strong> 3:00 PM - 4:40 PM</p>
                    <p><strong>Lugar:</strong> Virtual</p>
                    <p>
                        La sesión comenzó con una pregunta sobre las IA más conocidas por los participantes, generando un intercambio de ideas sobre 
                        su uso actual. Luego se abordó la importancia de crear buenos prompts para obtener mejores resultados en las herramientas de 
                        inteligencia artificial. Durante la actividad, se desarrolló un bot en Telegram como ejemplo práctico, se mencionó la plataforma 
                        Black Box y se explicó el concepto de Ingeniería de Prompts como una habilidad clave en el trabajo con modelos de IA.
                    </p>
                </div>
