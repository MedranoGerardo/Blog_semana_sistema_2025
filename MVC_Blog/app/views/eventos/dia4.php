                <div class="activity">
                    <h4>1. Feria de logros</h4>
                    <p><strong>Hora:</strong> 9:00 AM - 12:00 PM</p>
                    <p><strong>Lugar:</strong> Edificio Minerva</p>
                    <p>
                        La feria reunió una amplia gama de proyectos innovadores creados por estudiantes de todos los niveles académicos,
                        desde primer hasta quinto año. Durante la jornada, los asistentes tuvieron la oportunidad de interactuar directamente
                        con los desarrolladores, explorar las funcionalidades y objetivos de cada propuesta, y emitir su voto por los proyectos
                        más sobresalientes. Entre las presentaciones destacaron aplicaciones móviles, sistemas web, videojuegos interactivos y
                        diversas soluciones tecnológicas orientadas a mejorar la vida cotidiana, demostrando el talento, la creatividad y el
                        compromiso de los futuros profesionales en ingeniería de sistemas.
                    </p>
                </div>

                <div class="activity">
                    <h4>2. Comelona de Pupusas</h4>
                    <p><strong>Hora:</strong> 12:00 PM - 2:00 PM</p>
                    <p><strong>Lugar:</strong> Edificio Minerva</p>
                    <p>
                        La Comelona de Pupusas fue un evento lleno de sabor, energía y camaradería, que reunió a estudiantes y docentes en un 
                        ambiente festivo e inolvidable. Durante la jornada, los participantes degustaron una amplia variedad de pupusas tradicionales,
                        entre ellas las clásicas de queso, frijoles y chicharrón, celebrando así uno de los platillos más representativos de la
                        gastronomía salvadoreña. Más allá de la competencia, la actividad se convirtió en un espacio para compartir, reír y fortalecer 
                        los lazos de comunidad, cerrando el día con un toque de diversión y orgullo universitario. El gran ganador de la competencia fue 
                        el Ing. Guandique, quien se llevó el reconocimiento tras disfrutar nada menos que 16 pupusas, consolidándose como el campeón 
                        indiscutible de la jornada.
                    </p>
                </div>
