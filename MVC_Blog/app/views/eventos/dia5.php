                    <h4>1. Bingo Tecnológico</h4>
                    <p><strong>Hora:</strong> 9:00 AM - 12:00 PM</p>
                    <p><strong>Lugar:</strong> Auditorio 1</p>
                    <p>
                        El Bingo Tecnológico fue una actividad llena de diversión y emoción, donde los participantes tuvieron la oportunidad
                        de ganar diversos premios relacionados con la tecnología. Entre los premios destacados se encontraban audífonos,
                        power banks, vales de compra, camisetas, gorras y otros artículos que patrocinaron Acopus, que fueron muy apreciados por los
                        asistentes. La dinámica del bingo permitió a los estudiantes interactuar entre sí, fomentando un ambiente de amistad
                        y entusiasmo mientras esperaban ansiosos cada número anunciado. La actividad fue un éxito rotundo, dejando a todos
                        con ganas de más y cerrando la mañana con sonrisas y premios en mano.
                    </p>
                </div>
                
