                    <p><strong>Hora:</strong> 1:00 PM - 4:00 PM</p>
                    <p><strong>Lugar:</strong> Computo A</p>
                    <p>
                        El Torneo de Videojuegos fue uno de los momentos más esperados del día, donde los participantes demostraron
                        sus habilidades y estrategias en el popular juego de Clash Royale. La competencia atrajo a jugadores de
                        diferentes niveles, desde principiantes hasta expertos, creando un ambiente vibrante y lleno de adrenalina.
                        Los enfrentamientos fueron intensos y emocionantes, con cada jugador dando lo mejor de sí para avanzar en
                        el torneo. Los espectadores también disfrutaron del evento, animando a sus favoritos y compartiendo la emoción
                        de cada partida. Al final del torneo, se premiaron a los mejores jugadores, reconociendo su destreza y dedicación
                        al juego dejando como ganador a Mike Cruz.
                    </p>
